# Initialize adjacency matrix
size = len(restaurants)
adj_matrix = np.zeros((size, size))

# Fill the adjacency matrix
for match in matches:
    rest1, rest2, score = match
    idx1 = index_map[rest1]
    idx2 = index_map[rest2]
    # print(score)
    if float(score) > 1:
        score = float(score) / 2
    adj_matrix[idx1][idx2] = float(score)
    adj_matrix[idx2][idx1] = float(score)  # Assuming undirected graph

# Normalize the adjacency matrix
