# Initialize the adjacency matrix
adjacency_matrix = {}

def find_similarities():
    # Get all restaurants from the database and convert to a list
    restaurants = list(restaurants_collection.find())

    # Iterate over each restaurant
    for i, restaurant in enumerate(restaurants):
        restaurant_name = restaurant['name']
        print(f'Current Restaurant: {restaurant_name}\n')

        # Get the tags for the current restaurant
        tags = restaurant['tags']

        # Initialize the adjacency matrix for the current restaurant
        adjacency_matrix[restaurant_name] = {}

        # Iterate over each other restaurant
        for j in range(i + 1, len(restaurants)):
            other_restaurant = restaurants[j]
            other_restaurant_name = other_restaurant['name']
            print(f'Comparing {restaurant_name} to {other_restaurant_name}...')

            # Get the tags for the other restaurant
            other_tags = other_restaurant['tags']

            # Initialize the similarity score
            similarity_score = 0
            ignore = 0

            # Iterate over each tag in the current restaurant
            for tag in tags:
                # Iterate over each tag in the other restaurant
                for other_tag in other_tags:

                    for process_tag in tag.split('-'):
                        for other_process_tag in other_tag.split('-'):
                            # Find the similarity between the two tags
                            # print(f'{process_tag} vs {other_tag}')
                            similarity = find_similarity(process_tag.lower(), other_process_tag.lower())
                            # print(similarity)

                            # One of the words does not exist in the dictionary, do not count the comparison
                            if similarity == -1:
                                ignore += 1
                            else:
                                # Add the similarity to the total similarity score
                                similarity_score += similarity

            average_similarity = similarity_score / (len(tags) * len(other_tags) - ignore)

            print(average_similarity)

            # Add the average similarity to the adjacency matrix in both directions
            adjacency_matrix[restaurant_name][other_restaurant_name] = average_similarity
            adjacency_matrix.setdefault(other_restaurant_name, {})[restaurant_name] = average_similarity

